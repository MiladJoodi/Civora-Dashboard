function getTypeForTitle(title: string): Contract["type"] {
  for (const [type, titles] of Object.entries(typeMap)) {
    if (titles.includes(title)) return type as Contract["type"]
  }
  return "construction"
}

function generateContract(index: number): Contract {
  const title = contractTitles[index % contractTitles.length]
  const type = getTypeForTitle(title)

  // Distribute statuses: ~15 active, ~10 pending, ~15 completed, ~5 cancelled
  let status: Contract["status"]
  if (index < 15) status = "active"
  else if (index < 25) status = "pending"
  else if (index < 40) status = "completed"
  else status = "cancelled"

  // Progress based on status
  let progress: number
  switch (status) {
    case "active":
      progress = randomInt(15, 85)
      break
    case "pending":
      progress = 0
      break
    case "completed":
      progress = 100
      break
    case "cancelled":
      progress = randomInt(5, 40)
      break
  }

  // Value between 500M and 200B Rial
  const value = randomInt(5, 2000) * 100_000_000

  const startDate = randomJalaliDate(1402, 1403)
  const endDate = randomJalaliDate(1403, 1404)

  return {
    id: generateId(),
    title,
    contractor: randomItem(contractors),
    projectName: randomItem(projectNames),
    value,
    startDate,
    endDate,
    status,
    progress,
    type,
    attachments: randomInt(0, 12),
    lastUpdate: randomJalaliDate(1403, 1404),
    description: randomItem(descriptions),
  }
}

